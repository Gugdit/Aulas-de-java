package Aula05.exercicios;

public class exercicio1 {
    public static void main(String[] args) {
        System.out.println("-------------------Exercício 1: Contagem regressiva-------------------");
        int contador = 10;
        while (contador >= 1) {
            System.out.println("Número " + contador);
            contador--;

        }
        System.out.println("Fim!");
    }
}
